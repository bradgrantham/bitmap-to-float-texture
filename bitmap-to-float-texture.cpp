    // one pixel has 23bit * 4 = 92 bits
    // can store one 56-pixel (56-bit) glyph in a single pixel

template <typename TYPE>
TYPE fromBits(unsigned int u) {
    static_assert(sizeof(TYPE) == sizeof(unsigned int));
    union bits {unsigned int u; TYPE x;} x;

    x.u = u;

    return x.x;
}

